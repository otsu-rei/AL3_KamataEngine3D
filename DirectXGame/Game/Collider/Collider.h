#pragma once

////////////////////////////////////////////////////////////////////////////////////////////
// Collider class
////////////////////////////////////////////////////////////////////////////////////////////
class Collider {
public:

	//=========================================================================================
	// public methods
	//=========================================================================================

	//! @brief 衝突したら呼び出される関数
	virtual void OnCollision() = 0;

	//! @brief ワールド座標を取得
	virtual Vector3f GetWorldPosition() const = 0;

	float GetRadius() const { return radius_; }

	void SetRadius(float radius) { radius_ = radius; }

private:

	//=========================================================================================
	// private variables
	//=========================================================================================

	float radius_ = 0.0f; // default

};